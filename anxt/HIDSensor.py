    def __init__(self, nxt, port = DEFAULT_DIGITAL_PORT, i2c_addr = 0x04):
        DigitalSensor.__init__(self, nxt, port, i2c_addr)
        
    def set_modifier(self, modifier):
        self.set_addr_param("hid")
        return int(self.nxt.libanxt.nxt_hid_set_modifier(self.nxt.handle, self.port-1, self.modifiers[modifier]))==0

    def set_key(self, key, direct=True):
        self.set_addr_param("hid")
        if (direct):
            k = self.keys[key]
        else:
            k = ord(key)

        return int(self.nxt.libanxt.nxt_hid_set_key(self.nxt.handle, self.port-1, k))==0

    def transmit(self):
        self.set_addr_param("hid")
        return int(self.nxt.libanxt.nxt_hid_transmit(self.nxt.handle, self.port-1))==0

    def set_mode(self, mode = "ASCII"):
        self.set_addr_param("hid")
        return int(self.nxt.libanxt.nxt_hid_set_mode(self.nxt.handle, self.port-1, self.hidmodes[mode]))==0

    def send_str(self, text):
        self.set_addr_param("hid")
        return int(self.nxt.libanxt.nxt_hid_send_str(self.nxt.handle, self.port-1, c_char_p(text)))==0
